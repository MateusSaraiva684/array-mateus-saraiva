# Lista com 5 nomes de colegas da turma
nomes = ["João", "Maria", "Carlos", "Ana", "Fernanda"]

# Mostrando elementos específicos
print("Primeiro nome da lista:", nomes[0])        # Primeiro elemento
print("Terceiro nome da lista:", nomes[2])        # Terceiro elemento
print("Último nome da lista:", nomes[-1])         # Último elemento usando índice negativo

# Desafio extra: Verificar se "Carla" está na lista
if "Carla" in nomes:
    print("Carla está na lista!")
else:
    print("Carla não foi encontrada na lista.")
